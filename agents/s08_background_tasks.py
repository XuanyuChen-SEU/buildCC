"""
s08_background_tasks.py - 后台任务

在后台线程中运行命令。每次调用 LLM 之前，
都会先清空通知队列并把结果投递回来。

    主线程                     后台线程
    +-----------------+        +-----------------+
    | agent 循环      |        | 任务执行中      |
    | ...             |        | ...             |
    | [LLM 调用] <---+------- | 入队(result)    |
    |  ^清空队列      |        +-----------------+
    +-----------------+

    时间线：
    Agent ----[启动 A]----[启动 B]----[做其他工作]----
                 |              |
                 v              v
              [A 运行]       [B 运行]      （并行）
                 |              |
                 +---------- 通知队列 --> [结果被注入]

关键洞察："发射后不管（fire and forget）——命令运行时，agent 不会被阻塞。"
"""


